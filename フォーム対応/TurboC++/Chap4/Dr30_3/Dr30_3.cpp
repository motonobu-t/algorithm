void visit(int i,int j)
{
    m[i][j]=1;
    if (m[i][j+1]==0) visit(i,j+1);
    if (m[i+1][j]==0) visit(i+1,j);
    if (m[i][j-1]==0) visit(i,j-1);
    if (m[i-1][j]==0) visit(i-1,j);
}
void __fastcall TForm1::Button1Click(TObject *Sender)
{
    int i,j;

    visit(1,1);
	tinit();cls();
    for (i=0;i<10;i++){
        for (j=0;j<10;j++)
            printf("%2d",m[i][j]);
        printf("\n");
	}
}
